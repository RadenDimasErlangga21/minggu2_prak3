    <h2> Berita ke-{{$id}} </h2> 
    <p> Pada pertengahan Agustus kemarin, Intel baru saja mengumumkan bila mereka 
        siap berperang di pasar VGA dengan membawa divisi grafisnya yakni Intel Arc yang 
        direncanakan akan masuk pada kuartal pertama tahun 2022 mendatang. Melalui acara Intel 
        Gaming yang disiarkan melalui YouTube, Martyn Stroeve selaku Intel 
        Graphics product team mengumumkan bahwa Intel Arc akan mempunyai codename yang berbeda 
        tiap generasinya. Yakni Intel Alchemist, Battlemage, Celestial, dan Druid. 

    <p> Sayangnya ketika memasuki bagian akhir pada acara tersebut, Intel hanya memperlihatkan 
        sedikit penayangan bagaimana VGA mereka berjalan pada sejumlah game seperti Forza Horizon 4, 
        PUBG, Psychonauts 2, dan Metro Exodus. </p>

    <p>    Setelah hampir satu bulan lamanya sejak video pengenalan Intel Arc rilis, baru-baru ini 
        Videocardz telah mendapatkan bocoran berupa sebuah slide presentasi yang memperlihatkan 
        harga beserta dengan kedudukan VGA Intel bersama kompetitornya. </p>

    <p> Mulai dari SOC2 (75W) dengan harga yang diperkirakan akan berkisar di 100 hingga 270 USD 
        atau sekitar 1,4 juta rupiah sampai 3,8 juta rupiah. Berdasarkan gambar yang Videocardz 
        perlihatkan, SOC2 sepertinya akan bersaing dengan GTX1650 Super milik kubu hijau. </p>

    <p> Berbeda dengan SOC2, SOC1 (175-225W) sepertinya akan ditujukan untuk para gamer yang 
        mengincar performa. Karena SOC1 ditempatkan pada harga sekitar 300 hingga 500 USD atau 
        sekitar 4,2 sampai 7 juta rupiah.</p>

    <p>    SOC1 ini juga nampaknya akan bersaing dengan sejumlah VGA teranyar milik kubu merah 
        maupun kubu hijau, seperti RTX 3060, Radeon RX 6600XT, RTX 3060 Ti, bahkan hingga RTX 3070 
        dan 6700XT. </p>

